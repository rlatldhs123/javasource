package Exam;

public class Calc {

  // 멤버변수는 없고 메소드만 가진 클래스를 만들어보자

  // int 타입의 2개의 변수를 받아서 덧셈 결과를 리턴하는  메소드

  int add(int num1, int num2) {
    return num1 + num2;
    // int 타입의 2개의 변수를 받아서 뺄셈 결과를 리턴하는  메소드

  } // 2개의 변수를 받는 양식

  int subtract(int num1, int num2) {
    return num1 - num2;
    // int 타입의 2개의 변수를 받아서 곱하기 결과를 리턴하는  메소드

  } // 2개의 변수를 받는 양식

  int multiply(int num1, int num2) {
    return num1 * num2;
    // int 타입의 2개의 변수를 받아서 나눗셈 결과를 리턴하는  메소드

  } // 2개의 변수를 받는 양식

  int divide(int num1, int num2) {
    return num1 / num2;
  } // 2개의 변수를 받는 양식
}
